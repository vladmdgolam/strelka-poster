class Sphere {    
    constructor({radius, color, position, weight}) {
        this.radius = radius
        this.color = color
        this.position = position
        this.weight = weight 
    }


}

class Cube {    
    constructor({width, height, color, position, weight}) {
        this.width = width
        this.height = height
        this.color = color
        this.position = position
        this.weight = weight 
    }

    
}